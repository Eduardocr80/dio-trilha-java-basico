public class Operadores {
    public static void main(String[] args) {
        // concatenacao
        String concatenacao = "?";

        

        concatenacao = 1+1+1+"1";
        
        System.out.println(concatenacao);

        concatenacao = 1+"1"+1+1;
        
        System.out.println(concatenacao);

        concatenacao = 1+"1"+1+"1";
        
        System.out.println(concatenacao);

        concatenacao = "1"+1+1+1;
        
        System.out.println(concatenacao);
        
        concatenacao = "1"+(1+1+1);
        
        System.out.println(concatenacao);







    }

}
